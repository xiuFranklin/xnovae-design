/**
 * Created by eeexi on 2019/7/29.
 *
 * 单一职责原则
 *
 * 定义：不要存在多于一个导致类变更的原因

 理解：比如：一个类负责了 2 个功能，在修改功能 1 时，导致功能 2 发生了故障；两个功能在需求变更的时候都有可能导致类变更，那么该类存在两个导致类变更的原因

 体现： 一个类、接口、方法只负责一项职责

 优点：

 降低类的复杂度
 提高类的可读性
 提高系统的可维护性
 降低变更引起的风险
 变更是必然的，单一职责原则遵循的好，可以降低变更引起的风险
 单一职责也适用于模块化设计
 */
